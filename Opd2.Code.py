CREDENTIALS_FILE = "credentials.json"
DEFAULT_USERNAME = "am3"
DEFAULT_PASSWORD = "2012"
DEFAULT_RECOVERY_PASSWORD = "e60_m5"

def load_credentials():
    if not os.path.exists(CREDENTIALS_FILE):
        credentials = {
            "username": DEFAULT_USERNAME,
            "password": DEFAULT_PASSWORD,
            "recovery_password": DEFAULT_RECOVERY_PASSWORD
        }
        with open(CREDENTIALS_FILE, "w", encoding="utf-8") as f:
            json.dump(credentials, f, ensure_ascii=False, indent=4)
        return credentials
    try:
        with open(CREDENTIALS_FILE, "r", encoding="utf-8") as f:
            data = json.load(f)
        return {
            "username": data.get("username", DEFAULT_USERNAME),
            "password": data.get("password", DEFAULT_PASSWORD),
            "recovery_password": data.get("recovery_password", DEFAULT_RECOVERY_PASSWORD)
        }
    except Exception:
        return {
            "username": DEFAULT_USERNAME,
            "password": DEFAULT_PASSWORD,
            "recovery_password": DEFAULT_RECOVERY_PASSWORD
        }

def save_credentials(username, password, recovery_password=None):
    existing = load_credentials()
    credentials = {
        "username": username,
        "password": password,
        "recovery_password": recovery_password if recovery_password is not None else existing.get("recovery_password", DEFAULT_RECOVERY_PASSWORD)
    }
    with open(CREDENTIALS_FILE, "w", encoding="utf-8") as f:
        json.dump(credentials, f, ensure_ascii=False, indent=4)

def authenticate_user():
    credentials = load_credentials()
    attempts = 0
    while attempts < 3:
        username = input("🔐 اسم المستخدم: ").strip()
        password = input("🔑 كلمة المرور: ").strip()
        if username == credentials["username"] and password == credentials["password"]:
            print("✅ تم تسجيل الدخول بنجاح.")
            return True
        attempts += 1
        remaining = 3 - attempts
        print(f"❌ اسم المستخدم أو كلمة المرور غير صحيحان. تبقى {remaining} محاولة.")

    forgot = input("هل نسيت كلمة المرور؟ (نعم/لا): ").strip().lower()
    if forgot in ['نعم', 'y', 'yes', 'ايوه', 'ايوا', 'أيوه']:
        recovery_attempts = 0
        while recovery_attempts < 3:
            recovery = input("🔐 ادخل كلمة السر الاحتياطية: ").strip()
            if recovery == credentials["recovery_password"]:
                print("✅ تم تسجيل الدخول باستخدام كلمة السر الاحتياطية.")
                return True
            recovery_attempts += 1
            remaining_recovery = 3 - recovery_attempts
            print(f"❌ كلمة السر الاحتياطية خاطئة. تبقى {remaining_recovery} محاولة.")
    print("⚠️ فشل تسجيل الدخول. سيتم إغلاق البرنامج.")
    return False

