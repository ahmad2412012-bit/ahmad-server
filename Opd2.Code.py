def load_custom_companies():
    """تحميل الشركات المخصصة من الملف"""
    if not os.path.exists(CUSTOM_COMPANIES_FILE):
        return {}, {}
    try:
        with open(CUSTOM_COMPANIES_FILE, 'r', encoding='utf-8') as f:
            data = json.load(f)
            return data.get("db_files", {}), data.get("company_names", {})
    except:
        return {}, {}

def save_custom_companies(custom_db_files, custom_company_names):
    """حفظ الشركات المخصصة في الملف"""
    data = {
        "db_files": custom_db_files,
        "company_names": custom_company_names
    }
    with open(CUSTOM_COMPANIES_FILE, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=4)

def get_all_companies():
    """الحصول على جميع الشركات (الأصلية والمخصصة)"""
    custom_db, custom_names = load_custom_companies()
    all_db_files = {**DB_FILES, **custom_db}
    all_company_names = {**COMPANY_NAMES, **custom_names}
    return all_db_files, all_company_names


